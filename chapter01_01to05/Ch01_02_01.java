package chapter01_01to05;
/*
 * # 연산자
 * 1. 산술연산자 
 * 더하기 빼기 곱하기 나누기 나머지
 */
public class Ch01_02_01 {

	public static void main(String[] args) {

		// 정수 / 정수 = 정수
		// 정수 / 실수 = 실수
		// 실수 / 정수 = 실수
		System.out.println(10/3);   // 3 
		System.out.println(10/3.0); // 3.3333
		System.out.println(10.0/3); // 3.3333
		
		System.out.println(10.0%3); // 1.0
		System.out.println(10%3.0); // 1.0
		
	}

}
